package cl.ravenhill.stickfix.modes

/**
 * Represents the private mode settings for a user in the Stickfix bot application.
 * The `PrivateMode` enum class defines two possible states for private mode: `ENABLED` and `DISABLED`.
 */
enum class PrivateMode {
    /**
     * Indicates that private mode is enabled.
     */
    ENABLED,

    /**
     * Indicates that private mode is disabled.
     */
    DISABLED
}

