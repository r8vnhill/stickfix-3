package cl.ravenhill.stickfix.modes

/**
 * Represents the shuffle mode for a user's state in the Stickfix bot application. This enum defines two possible states
 * for shuffling stickers: `ENABLED` and `DISABLED`.
 */
enum class ShuffleMode {
    /**
     * Indicates that shuffle mode is enabled.
     */
    ENABLED,

    /**
     * Indicates that shuffle mode is disabled.
     */
    DISABLED
}
